#include "libft.h"

char	*ft_strdup(const char *s1)
{
	int		i;
	int		s1len;
	char	*str;

	i = 0;
	s1len = ft_strlen(s1);
	str = malloc((s1len + 1) * sizeof(char));
	if (!str)
		return (NULL);
	while (s1[i] != '\0')
	{
		str[i] = s1[i];
		i++;
	}
	str[i] = '\0';
	return (str);
}
